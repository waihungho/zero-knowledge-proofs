// Helper to convert map to string for transcript appending
func mapToString(m map[WireID]*big.Int) string {
	var parts []string
	for k, v := range m {
		parts = append(parts, fmt.Sprintf("%d:%s", k, v.String()))
	}
	return strings.Join(parts, ",")
}
